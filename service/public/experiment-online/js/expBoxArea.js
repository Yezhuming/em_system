function node(x,y)
{//X,Y保存当前点坐标，value为f(n)的值，father为该点的父节点
	this.X = x;
	this.Y = y;
	this.value = 0;
	this.father = -1;
}

function manhattan(nowPoint, endPoint){
	var X = Math.abs(nowPoint.X - endPoint.X);
	var Y = Math.abs(nowPoint.Y - endPoint.Y);
	return X+Y/4;
}

function AStar(startpoint, endpoint)
{
	
	var openList = new Array(),closeList = new Array();
	var result = new Array();
	var isfinish = false;																				//是否结束，false表示还未结束
	var currentPoint,index;
	if(startpoint.X != endpoint.X || startpoint.Y != endpoint.Y)										//将起始点加入openList
	{
		openList.push(startpoint);
		while(!isfinish && openList.length!=0)															//未找到终点或者openList不等于0
		{
			currentPoint = openList.pop();																//从openList中获得最小值
			closeList.push(currentPoint);																//放入closeList中
			var surroundPoint = surroundingPoint(currentPoint);											//获取附近的四个点
			for(var i = 0; i < surroundPoint.length; i++)
			{					
				if(surroundPoint[i].X == endpoint.X && surroundPoint[i].Y == endpoint.Y)
				{
					surroundPoint[i].father = currentPoint;
					closeList.push(surroundPoint[i]);
					isfinish = true;
					break;
				}
				else
				{
					if(surroundPoint[i].X >= minRow && surroundPoint[i].X <= maxRow
					&& surroundPoint[i].Y >= minCol && surroundPoint[i].Y <= maxCol
					&& expBoxMap[surroundPoint[i].X][surroundPoint[i].Y] == 0)							//在地图范围内，不是障碍物
					{
						if(existList(surroundPoint[i],closeList) == -1)									//不在close中（未被寻找过）
						{	
							surroundPoint[i].value = currentPoint.value+1+manhattan(surroundPoint[i],endpoint);
							index = existList(surroundPoint[i],openList);
							if(index == -1)																//不在打开列表中（未被检索过）
							{
								surroundPoint[i].father = currentPoint;
								openList.push(surroundPoint[i]);
							}
							else																		//存在openList中
							{
								if(openList[index].value > surroundPoint[i].value)
								{
									openList[index].value = surroundPoint[i].value;
									openList[index].father = currentPoint;
								}
							}
						}
						openList.sort(sortValue);														//排序
					}
				}		
			}
		}
		for(var i = closeList.length-1; closeList[i].X!=startpoint.X || closeList[i].Y!=startpoint.Y;)
		{
			result.push(closeList[i]);
			currentPoint = closeList[i].father;
			index = existList(currentPoint,closeList);
			i = index;
		}
	}
	if(result.length != 0)
	{
		result.push(startpoint);
	}
	
	return result;
}


function surroundingPoint(point)
{
	var List = new Array();
	List.push(new node(point.X, point.Y-1));//左
	List.push(new node(point.X, point.Y+1));//右
	List.push(new node(point.X-1, point.Y));//上
	List.push(new node(point.X+1, point.Y));//下
	return List;
}


function existList(point, List)         //是否在某个列表中
{
	for(var i = 0 ; i < List.length ; i++)
	{
		if(point.X == List[i].X && point.Y == List[i].Y)
		{
			return i;
		}
	}
	return -1;
}


function sortValue(a,b)
{
	return b.value - a.value;
}